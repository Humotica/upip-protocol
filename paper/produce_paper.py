    env = capture_environment()
    print(f"  Host: {env['hostname']}")
    print(f"  Python: {env['python']}")

    # Step 3: Generate paper
    print("\n[3/3] Generating paper...")
    paper = generate_paper(results, env)

    output_path = "paper/PAPER.md"
    with open(output_path, "w") as f:
        f.write(paper)

    paper_hash = hashlib.sha256(paper.encode()).hexdigest()
    print(f"  Output: {output_path}")
    print(f"  Paper SHA-256: {paper_hash}")

    # Write verification sidecar
    verification = {
        "verification_hash": results["verification_hash"],
        "paper_hash": paper_hash,
        "environment": env,
        "results": {
            "fibonacci_hash": results["fibonacci_hash"],
            "prime_factors_hash": results["prime_factors_hash"],
        },
    }

    verify_path = "paper/VERIFICATION.json"
    with open(verify_path, "w") as f:
        json.dump(verification, f, indent=2)

    print(f"  Verification: {verify_path}")
    print(f"\n{'=' * 50}")
    print(f"Verification hash: {results['verification_hash']}")
