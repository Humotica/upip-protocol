def main():
    print("UPIP Paper Generator v1.0")
    print("=" * 50)

    # Step 1: Run deterministic computations
    print("\n[1/3] Running deterministic computations...")
    results = compute_verification_block()
    print(f"  Fibonacci(50) = {results['fibonacci_50_last']}")
    print(f"  Prime factors of primorial(25) = {results['prime_factors']}")
    print(f"  Verification hash: {results['verification_hash']}")

    # Step 2: Capture environment
