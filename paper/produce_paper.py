import hashlib
import json
import platform
import socket
import sys
from datetime import datetime, timezone


# ─── Deterministic computation ───────────────────────────────────────────

def fibonacci(n: int) -> list[int]:
    """Compute Fibonacci sequence. Pure function, deterministic."""
    a, b = 0, 1
    result = []
    for _ in range(n):
        result.append(a)
        a, b = b, a + b
    return result


def prime_factors(n: int) -> list[int]:
    """Compute prime factorization. Pure function, deterministic."""
    factors = []
    d = 2
    while d * d <= n:
        while n % d == 0:
            factors.append(d)
            n //= d
        d += 1
    if n > 1:
        factors.append(n)
    return factors


def compute_verification_block() -> dict:
    """Run deterministic computations and return results with hashes."""
    # Fibonacci
    fib_50 = fibonacci(50)
    fib_hash = hashlib.sha256(json.dumps(fib_50).encode()).hexdigest()

    # Prime factorization of primorial(25) — product of first 25 primes
    # A 37-digit number that decomposes back into exactly the first 25 primes
    target = 2305567963945518424753102147331756070
    factors = prime_factors(target)
    factors_hash = hashlib.sha256(json.dumps(factors).encode()).hexdigest()

    # Combined verification hash
    combined = f"{fib_hash}|{factors_hash}"
    verification_hash = hashlib.sha256(combined.encode()).hexdigest()

    return {
        "fibonacci_50_last": fib_50[-1],
        "fibonacci_50_sum": sum(fib_50),
        "fibonacci_hash": fib_hash,
        "prime_target": target,
        "prime_factors": factors,
        "prime_factors_hash": factors_hash,
        "verification_hash": verification_hash,
    }


# ─── Environment capture ────────────────────────────────────────────────

def capture_environment() -> dict:
    """Capture current machine environment."""
    return {
        "hostname": socket.gethostname(),
        "python": platform.python_version(),
        "platform": platform.platform(),
        "machine": platform.machine(),
        "processor": platform.processor() or "unknown",
    }


